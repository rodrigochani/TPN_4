# Pedir al usuario que ingrese el primer número
echo "Por favor, introduce el primer número:"
read num1

# Pedir al usuario que ingrese el segundo número
echo "Por favor, introduce el segundo número:"
read num2

# Sumar los números y almacenar el resultado
suma=$((num1 + num2))

# Imprimir el resultado
echo "La suma entre $num1 y $num2 es $suma."

